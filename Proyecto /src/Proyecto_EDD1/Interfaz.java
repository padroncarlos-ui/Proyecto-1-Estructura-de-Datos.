    /**
     * <p>Botón que muestra el JFileChooser para poder guardar un nuevo archivo de terminación .csv
     * que contenga los cambios realizados al grafo.</p>
     * Trabaja con el método guardarArchivo() de la clase GestorDeArchivos.
     * @param evt 
     */
    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        
        JFileChooser selector = new JFileChooser(); //Abre la ventana de selección de archivos de JFileChooser.
        selector.setDialogTitle("Guardar cambios en el archivo"); //Da un título a la ventana.
        
        if (selector.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) { //Verifica que opción eligió el usuario dentro de la ventana.
            File archivoDestino = selector.getSelectedFile(); //Obtiene el objeto tipo archivo que el usuario seleccionó o nombró.
            String ruta = archivoDestino.getAbsolutePath(); //Consigue la ruta de donde se guardó el archivo
            
            if (!ruta.toLowerCase().endsWith(".csv")) { //Verifica si el nombre del archivo no tiene la terminación .csv ignorando las mayúsculas.
                ruta += ".csv"; //Si no tiene la terminación esta se le agrega al final para asegurar el formato.
            } 
            GestorDeArchivos gestor = new GestorDeArchivos(this); //Instancia la clase encargada de manejar la entrada y salida de datos.
            gestor.guardarArchivo(ruta, this.grafoLogico); //Llama al método que recorre los datos.
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

