    /**
     * <p>Botón que trabaja en conjunto con el método cargarArchivo() de la clase
     * GestorDeArchivos.</p>
     * Abre el JFileChooser para seleccionar el archivo .csv que contiene las conexiones necesarias
     * para el grafo, cuando el archivo .csv sea cargado con éxito, se habilitarán los demás
     * botones de la interfaz, con el fin de evitar que sean pulsados sin antes cargar un archivo.
     * @param evt 
     */
    private void BtnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCargarActionPerformed
       
        JFileChooser selector = new JFileChooser(); //Abre la ventana de selección de archivos.
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos CSV", "csv"); //Hace por predeterminado que solo se visualicen los archivos con terminación .csv
        selector.setFileFilter(filtro); 
        
        if (selector.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { //Verifica si el usuario selecciono el archivo correcto.
            File archivoSeleccionado = selector.getSelectedFile(); //Obtiene el archivo seleccionado.

            try {
                this.grafo.clear(); //Reinicia el grafo.
                Estilo(); //Aplica los estilos predeterminados de la hoja de estilos del grafo.
                this.grafoLogico = new Grafo(this.grafo, this); //Instancia una nueva estructura de control para manejar los datos que están por leerse.
                
                GestorDeArchivos gestor = new GestorDeArchivos(this); //llama a la lógica de la clase GestorDeArchivos.
                boolean exito = gestor.cargarArchivo(archivoSeleccionado.getAbsolutePath(), this.grafoLogico);
                if (exito) {
                    mostrarYPosicionarGrafo(); //Llama al método encargado de la visualización del grafo.
                    configurarInterfaz(true); //Se vuelve true para poder cambiar los valores de visualización de los botones de la interfaz.
                    this.ConsolaTxt.setText("Archivo Cargado Con éxito.");
                }else {
                    configurarInterfaz(false); //En caso de que no se haya podido cargar el archivo no se muestran los demás botones de la interfaz.
                    this.ConsolaTxt.setText("Error: Terminación del archivo incorrecto.");
                }
         
            }catch (Exception e) {
                configurarInterfaz(false); //En caso de otros errores, no se muestran los demás botones de la interfaz.
                this.ConsolaTxt.setText("Error al cargar el archivo.");
            }
        }

    }//GEN-LAST:event_BtnCargarActionPerformed
   
