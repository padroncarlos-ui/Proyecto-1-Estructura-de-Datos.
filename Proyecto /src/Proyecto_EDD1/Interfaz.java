    /**
     * Método encargado de mover la visualización del Graphstream a la derecha de la pantalla
     * obteniendo su posición inicial y desplazandola a la derecha
     */
    private void mostrarYPosicionarGrafo() {
        Viewer viewer = grafo.display(); //Muestra el grafo.
        javax.swing.SwingUtilities.invokeLater(() -> { //Espera a que el grafo se dibuje para poder ejecutar lo que sigue.
            try {
                java.awt.Component view = (java.awt.Component) viewer.getDefaultView(); //Obtiene el componente visual para poder trabajar con el grafo como si fuera un objeto.
                java.awt.Window win = javax.swing.SwingUtilities.getWindowAncestor(view); //Busca donde se ubica el grafo cargado.
                if (win != null) { 
                    java.awt.Point p = win.getLocation(); //Guarda las coordenadas iniciales del grafo
                    win.setLocation(p.x + 290, p.y); //Desplaza las coordenadas obtenidas 290 pixeles a la derecha.
                }
            }catch (Exception e) { 
                this.ConsolaTxt.setText("Error al mover el grafo.");
            }
        });
    }

    /**
    * <p>Hoja de estilos para el Graphstream para cambiar las propiedades de los nodos
    * y de sus apuntadores.</p>
    * Los cambios se aplican al momento de iniciar  el Graphstream y después de realizar acciones de resaltar nodos.
    */
    public void Estilo(){
        String estiloGrafo =
                
                //Cambia las propiedades de todo lo que tiene que ver con los nodos.
                "node {" + 
                "fill-color: lightblue;" + //Color del nodo.
                "size: 60px;" + //Tamaño del nodo.
                "stroke-mode: plain;" + //Borde del nodo.
                "stroke-color: black;" + //Color del borde.
                "stroke-width: 2px;" + //Ancho del borde del nodo.
                "text-size: 16px;" + //Tamaño del texto dentro de los nodos.
                "}" +
                
                //Cambia las propiedades de los apuntadores entre nodos.
                "edge {" +                
                "size: 2px;" +  //Cambia el grosor de la línea   
                "text-size: 20px;" + //Tamaño del texto en los apuntadores que en este caso es el peso.
                "text-background-mode: rounded-box;" + //Le coloca un fondo blanco al peso para que se pueda diferenciar, sin este los números no se perciben dentro de las interacciones.
                "}" +        
                
                //Trabaja con el identificador de Hubs para poder resaltar el nodo con mayor conexiones.
                "node.hub {" +
                "fill-color: red;" + //Modifica solo el color del hub principal.
                "size: 75px;" + //Hace el hub más grande para que se note.
                "stroke-mode: plain;" + //Borde del Hub.
                "stroke-width: 3px;" + //Tamaño del borde.
                "}"  +
                
                //Trabaja con el algoritmo de Dijkstra para resaltar visualmente la ruta de menos peso en el grafo.
                "node.ruta {" +
                "fill-color: orange;" + //Color de los nodos que forman parte de la ruta.
                "size: 70px;" + //Tamaño de los nodos resaltados un poco más grande que los demás.
                "}" +                       
                "edge.ruta {" + //Cambia el color de los apuntadores de la ruta.
                "fill-color: darkorange;" +        
                "}" +
                
                //Aplica color a los diferentes componentes conexos para no repetir colores y se vea más ordenado.
                "node.comp1 { fill-color: lightgreen; }" + 
                "node.comp2 { fill-color: yellow; }" + 
                "node.comp3 { fill-color: lightpurple; }" + 
                "node.comp4 { fill-color: pink; }" + 
                "node.comp5 { fill-color: gray; }";   

        this.grafo.setAttribute("ui.stylesheet", estiloGrafo); //Aplica los atributos al grafo.
        this.grafo.setAttribute("layout.force", 0.1); //Separa los nodos para que no se vean tan juntos.
    }

    /**
     * Botón que llama al método obtenerBFS() de la clase GestorGrafos
     * y que resalta visualmente cuales son los componentes conexos 
     * con diferentes colores para cada grupo de proteínas.
     * @param evt 
     */
    private void BtnEncontrarCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEncontrarCompActionPerformed
        
        try {
            //Limpia visualmente los cambios del grafo.
            for (org.graphstream.graph.Node n : grafo){
                n.removeAttribute("ui.class");
            }
            grafo.edges().forEach(e -> e.removeAttribute("ui.class"));
            
            //Sincroniza la clase GestorGrafo con el estado actual del grafo en caso de modificaciones.
            int nTotal = grafoLogico.getCantidadProteinas(); //Cuantas proteínas hay en total.
            if (nTotal == 0){ //Si no hay datos entonces el proceso se detiene.
                return;
            } 
            
            GestorGrafos gestor = new GestorGrafos(nTotal); //Crea una nueva herramienta de cálculo para manejar la cantidad que haya de nodos.
            
            for (int i = 0; i < nTotal; i++) { //Recorre cada proteína
                Interaccion[] ady = grafoLogico.getAdyacentes(i); //Obtiene la lista de proteínas conectadas a ese nodo.
                for (int j = 0; j < grafoLogico.getNumAdyacentes(i); j++) {
                    int dest = grafoLogico.buscarPorNombre(ady[j].getDestino().getNombre()); //Busca el índice de la proteína conectada para poder usarla.
                    gestor.conectar(i, dest, (int)ady[j].getPeso()); //Crea el enlace en la estructura lógica.
                    gestor.conectar(dest, i, (int)ady[j].getPeso()); //Se hace dos veces de esta forma para asegurar la bidireccionalidad.
                    if (dest != -1) { //Verifica que el nodo destino realmente exista en el registro.
                        gestor.conectar(i, dest, (int)ady[j].getPeso()); //Registra la conexión de ida en el gestor de cálculos.
                        gestor.conectar(dest, i, (int)ady[j].getPeso()); //Registra la conexión de vuelta para asegurar que el grafo sea no dirigido.
                    }
                }
            }
            
            int[] mapaComponentes = gestor.obtenerBFS(); //Ejecuta el BFS (Búsqueda de anchura).
            int maxComp = 0; //Almacena el número total de grupos encontrados.
            for (int id : mapaComponentes){ //Busca cuál es el número de grupo más alto para saber cuántos grupos existen en total.
                if (id > maxComp){
                    maxComp = id;
                }
            } 
       
            for (int i = 0; i < nTotal; i++) { //Ciclo para aplicar cabios visuales a las proteínas involucradas.
                String nombre = grafoLogico.getProteinas()[i].getNombre(); //Obtiene el nombre de la proteína.
                org.graphstream.graph.Node node = grafo.getNode(nombre); //Busca el nodo de la proteína en Graphstream.
                
                if (node != null) {
                    int idComp = mapaComponentes[i]; //Obtiene el número de grupo al que pertenece esta proteína.
                    
                    //Aplica colores diferentes a cada componente conexo hasta un máximo de 5, después de eso los colores serán los iniciales.
                    if (idComp <= 5) {
                        node.setAttribute("ui.class", "comp" + idComp);
                    }else {
                        node.setAttribute("ui.class", "node"); //Se aplica el color que se le aplica a nodos iniciales en caso de que hayan muchos grupos conexos.
                    }
                }
            }

            //Generación de un reporte textual que se imprimirá en la consola de la interfaz.
            String reporte = "Análisis de complejos proteicos completado.\n";
            reporte += "Se han detectado " + maxComp + " grupos aislados.\n\n";
            
            for (int c = 1; c <= maxComp; c++) { //Recorre cada uno de los grupos encontrados.
                reporte += "Grupo #" + c + ": ";
                for (int i = 0; i < nTotal; i++) { //Revisa toda la lista de proteínas.
                    if (mapaComponentes[i] == c) { // Si la proteína pertenece al grupo actual, agrega el nombre de la proteína al reporte.
                        reporte += grafoLogico.getProteinas()[i].getNombre() + " ";
                    }
                }
                reporte += "\n";    //Salto de línea.                    
            }
            EscribirInfo(reporte); //Escribe el reporte en la consola.
            
        }catch (Exception e) {
            EscribirInfo("Error al identificar componentes conexos.");
        }
    }//GEN-LAST:event_BtnEncontrarCompActionPerformed

