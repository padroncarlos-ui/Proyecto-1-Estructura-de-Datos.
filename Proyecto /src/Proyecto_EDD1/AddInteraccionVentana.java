    /**
     * Botón que una vez presionado tomará los datos ingresados y 
     * los guardará para mostrarlos en el grafo, cerrando la ventana.
     * @param evt 
     */
    private void BtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConfirmarActionPerformed

        //Limpia los atributos del grafo.
        for (org.graphstream.graph.Node n : GrafoPrincipal){
            n.removeAttribute("ui.class");
        }
        GrafoPrincipal.edges().forEach(e -> e.removeAttribute("ui.class"));
        
        //Obtención de los datos ingresados en los campos correspondientes.
        String p1 = Caja1.getSelectedItem().toString();
        String p2 = Caja2.getSelectedItem().toString();
        String PesoInput = PesoInteraccion.getText().trim();
        
        //Situación de caso en que el usuario no escriba el peso de la interacción en el cuadro en blanco
        if (PesoInput.isEmpty()) {
            Interfaz.EscribirInfo("Por favor, escriba el peso de la interacción en el espacio en blanco.");
            return;
        }
      
        //Situación de caso en que las dos proteínas sean iguales.
        if (p1.equals(p2)){
            Interfaz.EscribirInfo("No se puede crear una interacción entre una proteína y ella misma.");
            return;
        }       
        
        try{ //Bloque para construir texto a número
            int Peso = Integer.parseInt(PesoInput); //Convierte el texto del peso a un número entero.
            
            if (Peso <= 0) { //Situación de caso en que el peso sea 0
                Interfaz.EscribirInfo("El peso debe ser un número mayor a 0");
                return;
            }

            boolean exito = this.grafoLogico.agregarInteraccion(p1, p2, (double) Peso); //Guarda la conexión en la lógica del grafo
            if(exito){
                Interfaz.EscribirInfo("Interacción añadida con éxito");
                this.dispose(); //Cierra la ventana
            }
        }catch(NumberFormatException e){
            Interfaz.EscribirInfo("Error al añadir la interacción");       
        }
        
    }//GEN-LAST:event_BtnConfirmarActionPerformed

