    /**
     * Método que evita que el usuario escriba en el campo del peso algún símbolo
     * o letra, eliminandolo y garantizando que el usuario escriba únicamente números.
     * @param evt 
     */
    private void PesoInteraccionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PesoInteraccionKeyTyped
        
        char c = evt.getKeyChar(); // Captura la tecla presionada.
        
        if (!Character.isDigit(c) && c != java.awt.event.KeyEvent.VK_BACK_SPACE) { //Si la tecla presionada no es un número la borra.
            evt.consume();//Hace que la letra no aparezca
            
            if (Interfaz != null) { //Escribe en el cuadro de texto el motivo del error.
                Interfaz.EscribirInfo("Solo puedes escribir número, no escribas ni letras ni símbolos");
            }
        }
    }//GEN-LAST:event_PesoInteraccionKeyTyped

