    /**
     * Botón que una vez presionado tomará los datos ingresados y los guardará para mostrarlos en el grafo.
     * @param evt 
     */
    private void BtnConfirmarElimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConfirmarElimActionPerformed
 
        //Limpia las propiedades del grafo.
        for (org.graphstream.graph.Node n : GrafoPrincipal) {
            n.removeAttribute("ui.class");
        }
        GrafoPrincipal.edges().forEach(e -> e.removeAttribute("ui.class"));
        
        String ProteInput = NuevaProte.getText().trim(); //Obtiene el número de la proteína insertada.
        String PesoInput = NuevoPeso.getText().trim(); //Obtiene el peso de la proteína.
        
        if (ProteInput.isEmpty()) { //Caso en el que el espacio de la proteína esté vacio.
            Interfaz.EscribirInfo("Por favor, escriba el número de la nueva proteína en el espacio en blanco.");
            return; //Detiene el proceso.
        }
        
        if (PesoInput.isEmpty()) { //Caso en el que el espacio del peso esté vacio.
            Interfaz.EscribirInfo("Por favor escriba el número del peso de la proteína en el espacio en blanco.");
            return; //Detiene el proceso.
        }
    
        String idNueva = "P" + ProteInput; //Completa el nombre de la proteína para agregarle "P" antes del número y mantener el formato.
        
        if (grafoLogico.buscarPorNombre(idNueva) != -1) { //Revisa si la proteína ingresada no existe.
            Interfaz.EscribirInfo("Esta Proteína ya existe.");
            return;
        }
        
        //Obtiene las tres selecciones de las cajas.
        String s1 = CajaInteraccion1.getSelectedItem().toString();
        String s2 = CajaInteraccion2.getSelectedItem().toString();
        String s3 = CajaInteraccion3.getSelectedItem().toString();

        boolean Duplicado = false; //Variable para detectar duplicados en la selección de interacciones.

        if (!s1.equals("Ninguna") && !s1.isEmpty()){ //Revisa si la primera selección es "Ninguna"
            if (s1.equals(s2) || s1.equals(s3)){ //Compara la primera selección con las demás selecciones sin tomar en cuenta "Ninguna"
                Duplicado = true; //Marca como duplicado.
            }
        }
        
        if (!s2.equals("Ninguna") && !s2.isEmpty()){ //Revisa si la segunda selección es "Ninguna"
            if (s2.equals(s3)){ // Compara si es igual a la tercera.
                Duplicado = true; //Marca como duplicado.
            }    
        }
        
        if (Duplicado) { //Si hay duplicados, detener el proceso.
            Interfaz.EscribirInfo("No se puede repetir la misma proteína dos veces");
            return;
        }    
            
        //Crea el nuevo nodo y sus conexiones si no hay mas errores.
        try{           
            
            int PesoFull = Integer.parseInt(PesoInput); //Convierte el peso a número entero
        
            Proteina protNueva = new Proteina(idNueva); //Crea la proteína lógica
            this.grafoLogico.addProteina(protNueva); //Agrega la proteína a la lista del grafo.

            String[] selecciones = {s1, s2, s3}; //Agrupa las selecciones en un arreglo para facilitar el recorrido.
        
            //Crea los enlaces necesarios entre la nueva proteína y las proteínas seleccionadas.
            for (String seleccion : selecciones) { //Recorre las posibles 3 conexiones.
                if (!seleccion.equals("Ninguna") && !seleccion.isEmpty()) {
                    this.grafoLogico.agregarInteraccion(idNueva, seleccion, (double) PesoFull); //Crea el enlace entre la nueva proteína y las seleccionadas.
                }
            }

            Interfaz.EscribirInfo("Se ha añadido la proteína " + idNueva + " con peso de: " + PesoFull);
            this.dispose();

        } catch (Exception e) {
            Interfaz.EscribirInfo("Error al insertar la proteína.");
        }
    }//GEN-LAST:event_BtnConfirmarElimActionPerformed

