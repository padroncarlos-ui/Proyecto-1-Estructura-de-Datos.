    /**
     * Evita que el usuario pueda escribir letras en el cuadro de texto para 
     * elegir el número de la proteína.
     * @param evt 
     */
    private void NuevaProteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NuevaProteKeyTyped

        char c = evt.getKeyChar(); // Captura la tecla presionada.
        
        if (!Character.isDigit(c) && c != java.awt.event.KeyEvent.VK_BACK_SPACE) { //Si la tecla presionada no es un número la borra.
            evt.consume(); //Hace que la letra no aparezca.
            
            
            if (Interfaz != null) {      //Escribe en el cuadro de texto el motivo del error. 
                Interfaz.EscribirInfo("Error: Solo puedes escribir número, no escribas ni letras ni símbolos");
            }
        }
           
    }//GEN-LAST:event_NuevaProteKeyTyped

